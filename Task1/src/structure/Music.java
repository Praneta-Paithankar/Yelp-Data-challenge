package structure;

public class Music {
	private boolean dj;
	private boolean background_music;
	private boolean no_music;
	private boolean karaoke;
	private boolean live;
	private boolean video;
	private boolean jukebox;
	public boolean isDj() {
		return dj;
	}
	public void setDj(boolean dj) {
		this.dj = dj;
	}
	public boolean isBackground_music() {
		return background_music;
	}
	public void setBackground_music(boolean background_music) {
		this.background_music = background_music;
	}
	public boolean isNo_music() {
		return no_music;
	}
	public void setNo_music(boolean no_music) {
		this.no_music = no_music;
	}
	public boolean isKaraoke() {
		return karaoke;
	}
	public void setKaraoke(boolean karaoke) {
		this.karaoke = karaoke;
	}
	public boolean isLive() {
		return live;
	}
	public void setLive(boolean live) {
		this.live = live;
	}
	public boolean isVideo() {
		return video;
	}
	public void setVideo(boolean video) {
		this.video = video;
	}
	public boolean isJukebox() {
		return jukebox;
	}
	public void setJukebox(boolean jukebox) {
		this.jukebox = jukebox;
	}

}
